package Java;

public class Incremental_Decremental {

	public static void main(String[] args) {
	
		
		int a = 50;
		int b = 60;
		
		System.out.println("The sum of a and b is: " + (a+b));
		//incremental/Decremental++__
		a=a+5;
		a++;
		System.out.println(a);
		
		b=b-5;
		b--;
		System.out.println(b);
		
	}

}
